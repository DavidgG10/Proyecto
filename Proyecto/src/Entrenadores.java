    private void jTablaEntrenadoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablaEntrenadoresMouseClicked
        // TODO add your handling code here:
        int entrenadorSeleccionado;
        try{
            entrenadorSeleccionado=jTablaEntrenadores.getSelectedRow();
            if(entrenadorSeleccionado==-1){
                JOptionPane.showMessageDialog(null, "No se ha seleccionado ningun cliente");
            }else{
                String nom=(String) jTablaEntrenadores.getValueAt(entrenadorSeleccionado, 0);
                String ape=(String) jTablaEntrenadores.getValueAt(entrenadorSeleccionado, 1);
                String eda=(String) jTablaEntrenadores.getValueAt(entrenadorSeleccionado, 2);
                String mem=(String) jTablaEntrenadores.getValueAt(entrenadorSeleccionado, 3);
                
                
                txtNombre.setText(nom);
                txtApellido.setText(ape);
                txtEdad.setText(eda);
                cmbTipoEntrenad.setSelectedItem(mem);
           }
        }catch (Exception e){
        }
          
    }//GEN-LAST:event_jTablaEntrenadoresMouseClicked

